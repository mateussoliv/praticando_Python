# append Adiciona um item ao final

lista1 = ['Mateus', 'Santos']
lista1.append('Oliveira')

print(lista1)


# insert - Adiciona um item no indice escolhido
lista1.insert(3, 'Fortes')
print(lista1)

# pop - Remove do final ou do indice escolhido
lista1.pop(2)
print(lista1)

# del - apaga um indice

# clear limpa uma lista
lista1.clear()
print(lista1)
